import java.util.*;

import codex.common.utils.*;

//-------------------------------------------------------------------------------------
public class Uid {

	//---statics:
	private static Random random = new Random();

//-------------------------------------------------------------------------------------
    public Uid(){
    }

//-------------------------------------------------------------------------------------
	public static final long next(){
		long _uid = System.currentTimeMillis();
		_uid = (_uid << 24);
		_uid += random.nextInt(65535);
		return _uid;
	}

//-------------------------------------------------------------------------------------
	public static final String toHexString(long _uid){
		return "0x" + Long.toHexString(_uid);
	}

//-------------------------------------------------------------------------------------
	public static final String toShortHexString(long _uid){
		return Long.toHexString(_uid);
	}

//-------------------------------------------------------------------------------------
}
